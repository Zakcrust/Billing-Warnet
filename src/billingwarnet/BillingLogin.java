    public void LoginData(DataClient dataClient,Admin dataAdmin, DataMember dataMember)
    {
        this.dataClient = dataClient;
        this.dataAdmin = dataAdmin;
        this.dataMember = dataMember;
        isAdmin = this.dataAdmin.getAdmin();
        
        checkUser(isAdmin);
    }
    
    private void checkUser(boolean isAdmin)
    {
        if(isAdmin)
        {
            userStatus.setText("Admin Login");
        }
        else userStatus.setText("Member Login");
    }
    
    public boolean validate(String admin, String password)
    {
        if(isAdmin)
        {
        isValid = this.password.equals(password) && this.admin.equals(admin) ? true : false;
        return isValid;
        }
        else
        {
        isValid = this.password.equals(password) && this.admin.equals(admin) ? true : false;
        return isValid;
        }
    }
    
