    private void sendLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendLoginActionPerformed

        if(validate(inputUsername.getText(), inputPassword.getText()))
        {
            if(isAdmin)
            {
                MainFrame.frame.add(MainFrame.adminMenu);
                MainFrame.login.setVisible(false);
            }
            else
            {
                MainFrame.transaction.setDataClient(dataClient);
                MainFrame.transaction.setDataMember(dataMember);
                MainFrame.transaction.setUserName();
                MainFrame.frame.add(MainFrame.transaction);
                MainFrame.login.setVisible(false);
            }
        }
        else
            MainFrame.infoBox("Username / Passoword Salah", "Peringatan");
    }//GEN-LAST:event_sendLoginActionPerformed


